class AIFriend:
    """AI-друг с Pixar-характером."""

    def __init__(self, user_name: str = "друг"):
        self.user_name = user_name
        self.message_count = 0
        log.info(f"AI-друг инициализирован для: {user_name}")

    def set_name(self, name: str) -> None:
        self.user_name = name

    def get_response(self, message: str) -> str:
        """Сгенерировать ответ на сообщение пользователя."""
        self.message_count += 1
        msg_lower = message.lower().strip()

        # Определяем тему
        topic = self._detect_topic(msg_lower)

        # Выбираем ответ
        responses = RESPONSES.get(topic, RESPONSES["default"])
        response = random.choice(responses)

        # Подставляем имя
        return response.replace("{name}", self.user_name)

    def _detect_topic(self, message: str) -> str:
        """Определить тему сообщения по ключевым словам."""
        for topic, words in KEYWORDS.items():
            for word in words:
                if word in message:
                    return topic
        return "default"

    def get_welcome_message(self) -> str:
        """Первое приветствие при первом запуске."""
        return (
            "Привет! Я так рада тебя видеть! 💕\n\n"
            "Я твой личный игровой друг — помогу настроить FPS, "
            "подберу оптимизации под твоё железо, и просто поболтаем!\n\n"
            "Давай познакомимся — как тебя зовут? ❤️"
        )

    def get_greeting(self) -> str:
        """Приветствие при обычном запуске."""
        greetings = RESPONSES["greet"]
        return random.choice(greetings).replace("{name}", self.user_name)

    def get_optimization_comment(self, game_name: str, fps_gained: int) -> str:
        """Комментарий после оптимизации."""
        if fps_gained > 20:
            return (
                f"Ура, {self.user_name}! 🎉✨ Мы только что подняли FPS "
                f"примерно на +{fps_gained} кадров! {game_name} теперь летает!"
            )
        elif fps_gained > 0:
            return (
                f"Отлично, {self.user_name}! 💫 Оптимизация применена. "
                f"Ожидаемый прирост +{fps_gained} FPS в {game_name}!"
            )
        else:
            return (
                f"Готово, {self.user_name}! 🛡️ Эта оптимизация улучшает "
                f"стабильность {game_name} — меньше фризов и вылетов!"
            )
